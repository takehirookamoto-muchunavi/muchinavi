function buildCustomerContext(customer: Customer): string {
  const parts: string[] = ['【現在のお客様情報】'];
  parts.push(`- お名前: ${customer.display_name || '未取得'}さん`);
  parts.push(`- 現在のステージ: ${customer.stage}`);
  if (customer.family_structure) {
    parts.push(`- 家族構成: ${JSON.stringify(customer.family_structure)}`);
  }
  if (customer.life_stage) {
    parts.push(`- ライフステージ: ${customer.life_stage}`);
  }
  if (customer.area_preference) {
    parts.push(`- ご希望エリア: ${JSON.stringify(customer.area_preference)}`);
  }
  if (customer.budget_range) {
    parts.push(`- ご予算: ${JSON.stringify(customer.budget_range)}`);
  }
  if (customer.values_priority) {
    parts.push(`- 価値観の優先順位: ${JSON.stringify(customer.values_priority)}`);
  }
  return parts.join('\n');
}
